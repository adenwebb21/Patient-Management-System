    private void btn_addUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addUserActionPerformed
        // TODO add your handling code here:
        char temp = 'N';
        
        if(drp_type.getSelectedItem().toString().equals("Doctor"))
        {
            temp = 'D';
        }
        else if(drp_type.getSelectedItem().toString().equals("Secretary"))
        {
            temp = 'S';
        }
        
        try
        {
            spn_age.commitEdit();
        }
        catch(java.text.ParseException e)
        {};
        
        thisController.createNewUser(temp, txt_newFName.getText(), txt_newLName.getText(), (int)spn_age.getValue(), txt_newPassword.getText(), txt_newSex.getText());
        setUpTables();
    }//GEN-LAST:event_btn_addUserActionPerformed

